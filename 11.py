# –ó–∞–¥–∞—á–∞ 1. –ü–æ—Å—Ç—Ä–æ–π—Ç–µ –≥—Ä–∞—Ñ–∏–∫ —Ñ—É–Ω–∫—Ü–∏–∏
# ùëì ùë• = 5ùë•^2 + 10ùë• ‚àí 30
# –ü–æ –≥—Ä–∞—Ñ–∏–∫—É –æ–ø—Ä–µ–¥–µ–ª–∏—Ç–µ, –ø—Ä–∏ –∫–∞–∫–∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è x –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ.

from sympy import *
from random import randint as RI
from matplotlib import pyplot as plt


def Task1():
    x = symbols('x')
    func = 5*x*x + 10*x - 30

    negative_x = solve(func)
    print(f'–∑–Ω–∞—á–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã –Ω–∞ –æ—Ç—Ä–µ–∑–∫–µ: {negative_x}')

    plot(func)

Task1()

# –ó–∞–¥–∞—á–∞ 2. –ò–º–µ—é—Ç—Å—è –¥–∞–Ω–Ω—ã–µ –æ –ø–ª–æ—â–∞–¥–∏ –∏ —Å—Ç–æ–∏–º–æ—Å—Ç–∏ 15 –¥–æ–º–æ–≤.
# –†–∏–µ–ª—Ç–æ—Ä—É —Ç—Ä–µ–±—É–µ—Ç—Å—è —É–∑–Ω–∞—Ç—å –≤ –∫–∞–∫–∏—Ö –¥–æ–º–∞—Ö —Å—Ç–æ–∏–º–æ—Å—Ç—å 
# –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–≥–æ –º–µ—Ç—Ä–∞ –º–µ–Ω—å—à–µ 50000 —Ä—É–±–ª–µ–π.
# –ü—Ä–µ–¥–æ—Å—Ç–∞–≤—å—Ç–µ –µ–º—É –≥—Ä–∞—Ñ–∏—á–µ—Å–∫–∏–µ –¥–∞–Ω–Ω—ã–µ –æ —Å—Ç–æ–∏–º–æ—Å—Ç–∏ –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–≥–æ –º–µ—Ç—Ä–∞ –∫–∞–∂–¥–æ–≥–æ –¥–æ–º–∞
# –∏ —Å–ø–∏—Å–æ–∫ –ø–æ–¥—Ö–æ–¥—è—â–∏—Ö –µ–º—É –¥–æ–º–æ–≤, –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö –ø–æ –ø–ª–æ—â–∞–¥–∏.
# –î–∞–Ω–Ω—ã–µ –æ –¥–æ–º–∞—Ö —Å—Ñ–æ—Ä–º–∏—Ä—É–π—Ç–µ —Å–ª—É—á–∞–π–Ω—ã–º –æ–±—Ä–∞–∑–æ–º. 
# –ü–ª–æ—â–∞–¥–∏ –æ—Ç 100 –¥–æ 300 –∫–≤. –º–µ—Ç—Ä–æ–≤, —Ü–µ–Ω—ã –æ—Ç 3 –¥–æ 20 –º–ª–Ω.



area_houses = [RI(100, 300) for i in range(15)]
print(f'–ü–ª–æ—â–∞–¥–∏ –¥–æ–º–æ–≤: {area_houses}')

prices_houses = [RI(3, 20) for i in range(15)]
print(f'–¶–µ–Ω—ã –¥–æ–º–æ–≤: {prices_houses}')


fig,ax = plt.subplots()
ax.bar(area_houses, prices_houses)
plt.title("–ó–∞–≤–∏—Å–∏–º–æ—Å—Ç—å —Ü–µ–Ω—ã –¥–æ–º–∞ –æ—Ç –µ–≥–æ –ø–ª–æ—â–∞–¥–∏")
plt.ylabel("–¶–µ–Ω–∞ –≤ –º–ª–Ω")
plt.xlabel("–ü–ª–æ—â–∞–¥—å –≤ –∫–≤. –º–µ—Ç—Ä–∞—Ö")
plt.show()

houses_mean = []
for i in range(15):
    houses_mean.append(prices_houses[i]/area_houses[i] * 1000)
print(f'–°—Ä–µ–¥–Ω—è—è —Ü–µ–Ω–∞ –∑–∞ –∫–≤.–º –≤ —Ç—ã—Å. —Ä—É–±: {houses_mean}')

plt.bar(list(range(1,16)), houses_mean)
plt.title("–°—Ä–µ–¥–Ω—è—è —Ü–µ–Ω–∞ –∑–∞ –∫–≤.–º –≤ —Ç—ã—Å. —Ä—É–±")
plt.ylabel("–¶–µ–Ω–∞ –≤ —Ç—ã—Å. —Ä—É–±")
plt.xlabel("–Ω–æ–º–µ—Ä –¥–æ–º–∞")
plt.show()



houses = list(zip(area_houses, prices_houses, houses_mean))
houses = list(filter(lambda x: x[2]<50, houses))
result = sorted(houses, key=lambda x: x[0])
print(result)
print(f'—Å–ø–∏—Å–æ–∫ –¥–æ–º–æ–≤ (–ø–ª–æ—â–∞–¥—å, —Ü–µ–Ω–∞, —Ü–µ–Ω–∞ –∑–∞ –∫–≤.–º): {result}')
